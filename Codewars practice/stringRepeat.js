function repeatStr(n, s) {
  //create an empty container/condition for a string
  let str = "";
  //   for loop that loops over the index of a number
  for (let i = 0; i < n; i++);
  {
    //   replaces value of str to str + s which also sets the number of times the string appears
    str += s;
  }
  //   returns the value of str
  return str;
}
